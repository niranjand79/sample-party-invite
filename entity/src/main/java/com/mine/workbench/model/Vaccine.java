package com.mine.workbench.model;

public class Vaccine {

	private int vaccineId;
	private String vaccineGroup;
	private String vaccineName;
	private String vaccineComponenent;
	private int stateSuplied;
	private String ageRange;
	private String dose;
	private String route;
	private String manufacturer;
	private String cptCode;
	private String cvxCode;

	public int getVaccineId() {
		return vaccineId;
	}

	public void setVaccineId(int vaccineId) {
		this.vaccineId = vaccineId;
	}

	public String getVaccineGroup() {
		return vaccineGroup;
	}

	public void setVaccineGroup(String vaccineGroup) {
		this.vaccineGroup = vaccineGroup;
	}

	public String getVaccineName() {
		return vaccineName;
	}

	public void setVaccineName(String vaccineName) {
		this.vaccineName = vaccineName;
	}

	public String getVaccineComponenent() {
		return vaccineComponenent;
	}

	public void setVaccineComponenent(String vaccineComponenent) {
		this.vaccineComponenent = vaccineComponenent;
	}

	public int getStateSuplied() {
		return stateSuplied;
	}

	public void setStateSuplied(int stateSuplied) {
		this.stateSuplied = stateSuplied;
	}

	public String getAgeRange() {
		return ageRange;
	}

	public void setAgeRange(String ageRange) {
		this.ageRange = ageRange;
	}

	public String getDose() {
		return dose;
	}

	public void setDose(String dose) {
		this.dose = dose;
	}

	public String getRoute() {
		return route;
	}

	public void setRoute(String route) {
		this.route = route;
	}

	public String getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}

	public String getCptCode() {
		return cptCode;
	}

	public void setCptCode(String cptCode) {
		this.cptCode = cptCode;
	}

	public String getCvxCode() {
		return cvxCode;
	}

	public void setCvxCode(String cvxCode) {
		this.cvxCode = cvxCode;
	}

}
